package main

import (
	"flag"

	temporalv1 "git.maurice.fr/thomas/protoc-gen-go-tmprl/gen/temporal/v1"
	"git.maurice.fr/thomas/protoc-gen-go-tmprl/internal/generator"
	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"
)

var (
	flagParam *bool
)

func main() {
	var flags flag.FlagSet
	flagParam = flags.Bool("param", false, "")
	opts := &protogen.Options{
		ParamFunc: flags.Set,
	}

	opts.Run(func(gen *protogen.Plugin) error {
		for _, f := range gen.Files {
			if !f.Generate {
				continue
			}
			generateFile(gen, f)
		}
		return nil
	})
}

func generateFile(plugin *protogen.Plugin, file *protogen.File) *protogen.GeneratedFile {
	filename := file.GeneratedFilenamePrefix + "_tprl.pb.go"
	gen := plugin.NewGeneratedFile(filename, file.GoImportPath)
	gen.P("// Code generated by protoc-gen-go-tmprl. DO NOT EDIT.")
	gen.P()
	gen.P("package ", file.GoPackageName)
	gen.P()

	for _, s := range file.Services {
		if _, ok := proto.GetExtension(s.Desc.Options(), temporalv1.E_Service).(*temporalv1.WorkerOptions); !ok {
			// not a temporal service
			continue
		}

		err := generator.ServiceConstants(gen, s)
		if err != nil {
			plugin.Error(err)
		}

		err = generator.UnimplementedServiceInterface(gen, s)
		if err != nil {
			plugin.Error(err)
		}

		err = generator.Worker(gen, s)
		if err != nil {
			plugin.Error(err)
		}

		err = generator.Client(gen, s)
		if err != nil {
			plugin.Error(err)
		}
	}

	return gen
}
